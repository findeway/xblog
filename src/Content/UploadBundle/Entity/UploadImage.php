<?php

namespace Content\UploadBundle\Entity;

use Doctrine\ORM\Mapping as ORM;
use Symfony\Component\HttpFoundation\File\UploadedFile;
use Symfony\Component\Validator\Constraints as Assert;

/**
 * Image
 *
 * @ORM\Table()
 * @ORM\Entity
 */
class UploadImage
{
    /**
     * @var integer
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="name", type="string", length=255)
     */
    private $name;

    /**
     * @var string
     *
     * @ORM\Column(name="filepath", type="string", length=255)
     */
    private $filepath;

    /**
     * @var \DateTime
     *
     * @ORM\Column(name="uploadDateTime", type="datetime")
     */
    private $uploadDateTime;

    /**
     * @var string
     *
     * @ORM\Column(name="url", type="string", length=255)
     */
    private $url;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     * @return Image
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string 
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set filepath
     *
     * @param string $filepath
     * @return Image
     */
    public function setFilepath($filepath)
    {
        $this->filepath = $filepath;

        return $this;
    }

    /**
     * Get filepath
     *
     * @return string 
     */
    public function getFilepath()
    {
        return $this->filepath;
    }

    /**
     * Set uploadDateTime
     *
     * @param \DateTime $uploadDateTime
     * @return Image
     */
    public function setUploadDateTime($uploadDateTime)
    {
        $this->uploadDateTime = $uploadDateTime;

        return $this;
    }

    /**
     * Get uploadDateTime
     *
     * @return \DateTime 
     */
    public function getUploadDateTime()
    {
        return $this->uploadDateTime;
    }

    /**
     * Set url
     *
     * @param string $url
     * @return Image
     */
    public function setUrl($url)
    {
        $this->url = $url;

        return $this;
    }

    /**
     * Get url
     *
     * @return string 
     */
    public function getUrl()
    {
        return $this->url;
    }
    
    /**
     * @Assert\File(
     *     maxSize = "5M",
     *     mimeTypes = {"image/jpeg", "image/gif", "image/png", "image/tiff"},
     *     maxSizeMessage = "The maxmimum allowed file size is 5MB.",
     *     mimeTypesMessage = "Only the filetypes image are allowed."
     * )    
     */
    private $file;

    private $tempPath;
    
    /**
     * Sets file.
     *
     * @param UploadedFile $file
     */
    public function setFile(UploadedFile $file = null)
    {
        $this->file = $file;
        // check if we have an old image path
        if (!empty($this->filepath)) {
            // store the old name to delete after the update
            $this->tempPath = $this->filepath;
            $this->filepath = '';
        }
        if (null !== $this->getFile()) {
            // do whatever you want to generate a unique name
            $filename = md5_file($this->getFile()->getPathname());
            $this->setName($filename);
            $this->setFilepath($filename.'.'.$this->getFile()->getClientOriginalExtension());
        }
    }
    
    /**
     * Get file.
     *
     * @return UploadedFile
     */
    public function getFile()
    {
        return $this->file;
    }
    
    public function getAbsolutePath()
    {
        return null === $this->path
        ? null
        : $this->getUploadRootDir().'/'.$this->filepath;
    }
    
    public function getWebPath()
    {
        return null === $this->filepath
        ? null
        : $this->getUploadDir().'/'.$this->filepath;
    }
    
    protected function getUploadRootDir()
    {
        // the absolute directory path where uploaded
        // documents should be saved
        return __DIR__.'/../../../../web/'.$this->getUploadDir();
    }
    
    protected function getUploadDir()
    {
        // get rid of the __DIR__ so it doesn't screw up
        // when displaying uploaded doc/image in the view.
        return 'uploads/documents';
    }

    /**
     * @ORM\PrePersist()
     * @ORM\PreUpdate()
     */
    public function preUpload()
    {
    }
    
    /**
     * @ORM\PostPersist()
     * @ORM\PostUpdate()
     */
    public function upload()
    {
        if (null === $this->getFile()) {
            return;
        }
        // if there is an error when moving the file, an exception will
        // be automatically thrown by move(). This will properly prevent
        // the entity from being persisted to the database on error
        $this->getFile()->move($this->getUploadRootDir(), $this->getFilepath());
    
        // check if we have an old image
        if (!empty($this->tempPath)) {
            // delete the old image
            unlink($this->getUploadRootDir().'/'.$this->tempPath);
            // clear the temp image path
            $this->tempPath = '';
        }
        $this->file = null;
    }
    
    /**
     * @ORM\PostRemove()
     */
    public function removeUpload()
    {
        if ($file = $this->getAbsolutePath()) {
            unlink($file);
        }
    }
    
    function __construct($file = null)
    {
        $this->name = '';
        $this->url = '';
        $this->filepath = '';
        $this->tempPath = '';
        $this->uploadDateTime = new \DateTime();
        $this->setFile($file);
    }
}
